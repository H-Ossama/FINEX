const currencySymbols = {
  USD: '$',
  EUR: '€',
  MAD: 'MAD',
};

const humanizeMissingKey = (key: unknown): string => {
  if (typeof key !== 'string') return '';

  // Example: "subscription_upgrade_button.monthly" -> "Subscription Upgrade Button Monthly"
  const cleaned = key.replace(/[._]+/g, ' ').replace(/\s+/g, ' ').trim();
  if (!cleaned) return '';

  return cleaned
    .split(' ')
    .map(word => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ');
};

const looksLikeTranslationKey = (key: string): boolean => {
  // Most of this app's translation keys are snake_case or dot.notation.
  return key.includes('_') || key.includes('.');
};

const LocalizationContext = createContext<LocalizationContextType | undefined>(undefined);

const LANGUAGE_STORAGE_KEY = '@fintracker_language';
const CURRENCY_STORAGE_KEY = '@fintracker_currency';

export const LocalizationProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [language, setLanguageState] = useState<Language>('en');
  const [currency, setCurrencyState] = useState<Currency>('USD');

  useEffect(() => {
    loadPreferences();
  }, []);

  const loadPreferences = async () => {
    try {
      const savedLanguage = await AsyncStorage.getItem(LANGUAGE_STORAGE_KEY);
      const savedCurrency = await AsyncStorage.getItem(CURRENCY_STORAGE_KEY);

      if (savedLanguage && ['en', 'de', 'ar'].includes(savedLanguage)) {
        setLanguageState(savedLanguage as Language);
      }

      if (savedCurrency && ['USD', 'EUR', 'MAD'].includes(savedCurrency)) {
        setCurrencyState(savedCurrency as Currency);
      }
    } catch (error) {
      console.error('Error loading localization preferences:', error);
    }
  };

  const setLanguage = async (newLanguage: Language) => {
    try {
      setLanguageState(newLanguage);
      await AsyncStorage.setItem(LANGUAGE_STORAGE_KEY, newLanguage);
    } catch (error) {
      console.error('Error saving language preference:', error);
    }
  };

  const setCurrency = async (newCurrency: Currency) => {
    try {
      setCurrencyState(newCurrency);
      await AsyncStorage.setItem(CURRENCY_STORAGE_KEY, newCurrency);
    } catch (error) {
      console.error('Error saving currency preference:', error);
    }
  };

  const t = (key: string, params?: { [key: string]: string | number }): string => {
    if (typeof key !== 'string' || key.length === 0) return '';

    const languageTable = (translations[language] as any) || {};
    const englishTable = (translations.en as any) || {};

    const inLanguage = languageTable[key];
    const inEnglish = englishTable[key];

    // If this doesn't look like a translation key (e.g. "Settings", "pay", "monthly limit"),
    // treat it as already-user-facing text and don't warn.
    if (inLanguage == null && inEnglish == null && !looksLikeTranslationKey(key)) {
      return key;
    }

    // Prefer current language, then fallback to English, then a humanized key.
    let translation: string = String(inLanguage ?? inEnglish ?? humanizeMissingKey(key));

    if (__DEV__ && inLanguage == null && inEnglish == null) {
      // eslint-disable-next-line no-console
      console.warn(`[i18n] Missing translation for key "${key}" (lang=${language})`);
    }

    // Replace parameters in the translation string
    if (params) {
      Object.keys(params).forEach(paramKey => {
        const regex = new RegExp(`{${paramKey}}`, 'g');
        translation = translation.replace(regex, String(params[paramKey]));
      });
    }

    return translation;
  };

  const formatCurrency = (amount: number): string => {
    const symbol = currencySymbols[currency];
    const formattedAmount = Math.abs(amount).toFixed(2);

    // For Arabic and some currencies, format differently
    if (language === 'ar') {
      return `${formattedAmount} ${symbol}`;
    }

    if (currency === 'MAD') {
      return `${formattedAmount} ${symbol}`;
    }

    return `${symbol}${formattedAmount}`;
  };

  return (
    <LocalizationContext.Provider
      value={{
        language,
        currency,
        setLanguage,
        setCurrency,
        t,
        formatCurrency,
      }}
    >
      {children}
    </LocalizationContext.Provider>
  );
};

export const useLocalization = (): LocalizationContextType => {
  const context = useContext(LocalizationContext);
  if (!context) {
    throw new Error('useLocalization must be used within a LocalizationProvider');
  }
  return context;
};